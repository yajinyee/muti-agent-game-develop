CHAR_W = 8
CHAR_H = 8
CHARS_PER_ROW = 16
SCALE = 2  # 輸出 2x 放大（16x16 per char）


def generate_font_png():
    """生成字體 PNG 圖"""
    chars = sorted(FONT_DATA.keys(), key=lambda c: ord(c))
    rows = (len(chars) + CHARS_PER_ROW - 1) // CHARS_PER_ROW

    img_w = CHARS_PER_ROW * CHAR_W * SCALE
    img_h = rows * CHAR_H * SCALE
    img = Image.new("RGBA", (img_w, img_h), (0, 0, 0, 0))

    for i, char in enumerate(chars):
        col = i % CHARS_PER_ROW
        row = i // CHARS_PER_ROW
        bitmap = FONT_DATA[char]

        for y, row_bits in enumerate(bitmap):
            for x in range(8):
                if row_bits & (0x80 >> x):
                    # 放大 SCALE 倍
                    for dy in range(SCALE):
                        for dx in range(SCALE):
                            px_x = col * CHAR_W * SCALE + x * SCALE + dx
                            px_y = row * CHAR_H * SCALE + y * SCALE + dy
                            img.putpixel((px_x, px_y), (255, 255, 255, 255))

    return img, chars


def generate_fnt_file(chars, img_w, img_h, png_filename):
    """生成 .fnt 文件（BMFont 格式，Godot 4 支援）"""
    char_w = CHAR_W * SCALE
    char_h = CHAR_H * SCALE

    lines = []
    lines.append(f'info face="{FONT_NAME}" size={char_h} bold=0 italic=0 charset="" unicode=1 stretchH=100 smooth=0 aa=0 padding=0,0,0,0 spacing=1,1')
    lines.append(f'common lineHeight={char_h} base={char_h} scaleW={img_w} scaleH={img_h} pages=1 packed=0')
    lines.append(f'page id=0 file="{png_filename}"')
    lines.append(f'chars count={len(chars)}')

    for i, char in enumerate(chars):
        col = i % CHARS_PER_ROW
        row = i // CHARS_PER_ROW
        x = col * char_w
        y = row * char_h
        char_id = ord(char)
        lines.append(f'char id={char_id} x={x} y={y} width={char_w} height={char_h} xoffset=0 yoffset=0 xadvance={char_w+1} page=0 chnl=15')

    return '\n'.join(lines)


def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    print("=== 生成像素字體 ===\n")

    # 生成 PNG
    img, chars = generate_font_png()
    png_path = os.path.join(OUTPUT_DIR, f"{FONT_NAME}.png")
    img.save(png_path)
    print(f"  ✅ 字體 PNG: {png_path} ({img.size})")
    print(f"  字元數: {len(chars)}")

    # 生成 .fnt
    fnt_content = generate_fnt_file(chars, img.width, img.height, f"{FONT_NAME}.png")
    fnt_path = os.path.join(OUTPUT_DIR, f"{FONT_NAME}.fnt")
    with open(fnt_path, 'w', encoding='utf-8') as f:
        f.write(fnt_content)
    print(f"  ✅ 字體 FNT: {fnt_path}")

    # 生成 Godot 4 .import 文件（讓 Godot 識別）
